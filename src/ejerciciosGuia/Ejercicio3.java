package ejerciciosGuia;

public class Ejercicio3 {

    public static void main(String[] args) {

        int num1 = 4;
        int num2 = 7;
        int num3 = -5;

        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);
        System.out.println(-num1);
        System.out.println(+num3);
        System.out.println(num2++);
        System.out.println(num2);
        System.out.println(++num1);
        System.out.println(--num3);
        System.out.println(num3);
        System.out.println(num3--);
        System.out.println(num1 == num2);
        System.out.println(num1 != num2);
        System.out.println(num1 > num2);
        System.out.println(num1 >= num2);
        System.out.println(num1 < num2);
        System.out.println(num1 <= num2);
        System.out.println(num1 == num2);
    }
}
